#include <stdio.h>

void main(void)
{
	int num1;
	int num2;
	
	printf("Enter the first number: ");
	scanf("%d",&num1);
	
	printf("Enter the second number: ");
	scanf("%d",&num2);
	
	printf("The addition = %d \n",num1 + num2);
	printf("The subtraction = %d \n",num1 - num2);
	printf("The multiplication = %d \n",num1 * num2);
	printf("The division = %d \n",num1 / num2);
	
}
